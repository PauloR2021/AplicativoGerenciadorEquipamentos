    private void tabelaMaquinasMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMaquinasMouseReleased

        if (evt.getClickCount() == 2) {

            DefaultTableModel tabela = (DefaultTableModel) tabelaMaquinas.getModel();

            int numeroDeLinha = tabelaMaquinas.getSelectedRow();

            if (alterar == null) {
                alterar = new AlterarMaquinas();
                alterar.setVisible(true);

                alterar.RecebendoDados(
                        tabela.getValueAt(numeroDeLinha, 0).toString(),
                        tabela.getValueAt(numeroDeLinha, 1).toString(),
                        tabela.getValueAt(numeroDeLinha, 2).toString(),
                        tabela.getValueAt(numeroDeLinha, 3).toString(),
                        tabela.getValueAt(numeroDeLinha, 4).toString(),
                        tabela.getValueAt(numeroDeLinha, 5).toString(),
                        tabela.getValueAt(numeroDeLinha, 6).toString(),
                        tabela.getValueAt(numeroDeLinha, 7).toString(),
                        tabela.getValueAt(numeroDeLinha, 8).toString(),
                        tabela.getValueAt(numeroDeLinha, 9).toString(),
                        tabela.getValueAt(numeroDeLinha, 10).toString()
                );

            } else {
                alterar.setState(AlterarMaquinas.NORMAL);
                alterar.setVisible(true);
                alterar.RecebendoDados(
                        tabela.getValueAt(numeroDeLinha, 0).toString(),
                        tabela.getValueAt(numeroDeLinha, 1).toString(),
                        tabela.getValueAt(numeroDeLinha, 2).toString(),
                        tabela.getValueAt(numeroDeLinha, 3).toString(),
                        tabela.getValueAt(numeroDeLinha, 4).toString(),
                        tabela.getValueAt(numeroDeLinha, 5).toString(),
                        tabela.getValueAt(numeroDeLinha, 6).toString(),
                        tabela.getValueAt(numeroDeLinha, 7).toString(),
                        tabela.getValueAt(numeroDeLinha, 8).toString(),
                        tabela.getValueAt(numeroDeLinha, 9).toString(),
                        tabela.getValueAt(numeroDeLinha, 10).toString()
                );
            }

        }

    }//GEN-LAST:event_tabelaMaquinasMouseReleased

