    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
         DefaultTableModel tabela = (DefaultTableModel) tabelaMaquinas.getModel();

        try {
            ConexaoBanco banco = new ConexaoBanco();

            int numeroDeLinha = tabelaMaquinas.getSelectedRow();

            ConstrutorMaquinas maquinas = new ConstrutorMaquinas ();

            int id = Integer.parseInt(tabela.getValueAt(numeroDeLinha, 0).toString());

            maquinas.setId(id);

            banco.ExcluirMaquinas(maquinas);

            BuscarMaquina();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Erro Ao Excluir Impressora", "Erro", 0);

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

