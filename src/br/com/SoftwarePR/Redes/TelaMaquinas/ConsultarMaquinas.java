    private void BuscarMaquina() {

        String busca;

        busca = "%" + txtBuscar.getText() + "%";

        ConexaoBanco Banco = new ConexaoBanco();

        List<ConstrutorMaquinas> lista = Banco.ConsultarMaquinas(busca);

        DefaultTableModel tabela = (DefaultTableModel) tabelaMaquinas.getModel();

        tabela.setNumRows(0);

        for (ConstrutorMaquinas linha : lista) {
            tabela.addRow(new Object[]{
                linha.getId(),
                linha.getHostname(),
                linha.getColaborador(),
                linha.getUa(),
                linha.getEquipamento(),
                linha.getModelo(),
                linha.getSistema(),
                linha.getSsd(),
                linha.getRam(),
                linha.getSerial(),
                linha.getPatrimonio()

            });
        }
    }

